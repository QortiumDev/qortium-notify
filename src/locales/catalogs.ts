function preservePlaceholders(source: string, translated: string) {
  const placeholders = source.match(/\{\w+\}/g) ?? [];
  let placeholderIndex = 0;
  const normalized = translated.replace(/\{[^}]+\}/g, () => placeholders[placeholderIndex++] ?? '');

  return normalized + placeholders.slice(placeholderIndex).map((placeholder) => ` ${placeholder}`).join('');
}

function createCatalog(values: string[]): MessageCatalog {
  return Object.fromEntries(
    MESSAGE_KEYS.map((key, index) => [key, preservePlaceholders(EN_STRINGS[key], values[index] ?? '')]),
  ) as MessageCatalog;
}

export const CATALOGS: Record<Exclude<SupportedLanguage, 'en'>, MessageCatalog> = Object.fromEntries(
  Object.entries(LOCALIZED_VALUES).map(([language, values]) => [language, createCatalog(values)]),
) as Record<Exclude<SupportedLanguage, 'en'>, MessageCatalog>;

